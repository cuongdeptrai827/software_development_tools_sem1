import math

def area(r):
    return math.pi * r * r
'''
    Number r the radius of the circle
    return the value of square of the circle with formula S = pi * r^2 '''

def perimeter(r):
    ''' Number r is the radius of the circle
        return the value of perimeter of the circle with formula S = pi * 2r
    '''
    return 2 * math.pi * r

